package core1.Basics;

public class variable {
    public static void main(String[] args) {
        /*
        * double float char
        * */
        int num1 = 0b110;
		int num2 = 110;
		int num3 = 0127;
		int num4 = 0x110A;

		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);
		System.out.println("num3 = " + num3);
		System.out.println("num4 = " + num4);
    }
}
